function doDescription(key, dict) {
    var desc = ``;

    for(var i=0;i<dict.length;i++) {
        var data = dict[i];

        desc += `
            <div style="display: inline-block; width: 32%; text-align: center;">
            <img src="https://pvis2023.github.io//pvis2023/assets/images/oc/${data['first_name']}_${data['last_name']}.jpg" class="circle" width="150" height="150"><br>
        `;

        if(Mobile()) {
            var aff = data['mobile-affiliation'].split(';');
            var cnt = data['mobile-country'].split(';');

            desc += `
                <strong> ${data['first_name']} <br> ${data['last_name']} </strong><br>
            `;

            if(aff.length >= 2) {
                desc += `
                    ${aff[0]} <br> ${aff[1]} <br>
                `;
            }
            else {
                desc += `
                    ${aff[0]} <br>
                `;
            }

            if(cnt.length >= 2) {
                desc += `
                    ${cnt[0]} <br> ${cnt[1]}
                `;
            }
            else {
                desc += `
                    ${cnt[0]}
                `;
            }
        }
        else {
            desc += `
                <strong> ${data['first_name']} ${data['last_name']} </strong><br>
                ${data['affiliation']}<br>${data['country']}
            `;
        }

        desc += `</div>`;

        if(i==2) desc += '<br>'
    }

    document.getElementsByClassName(key)[0].innerHTML = desc;
}

function generateCommitteeTable(isEmail, className, datas) {
    var elem = document.getElementsByClassName(className)[0];

    var htmls = ``;
    if(isEmail) {
        htmls += `
            <thead>
                <tr>
                    <th>Name</th>
                    <th>Affilation</th>
                    <th>Email</th>
                </tr>
            </thead>
        `;
    } else {
        htmls += `
            <thead>
                <tr>
                    <th>Name</th>
                    <th>Affilation</th>
                </tr>
            </thead>
        `;
    }
    
    htmls += `<tbody>`;
    for(var data of datas) {
        htmls += `
            <tr>
                <td>${data['name']}</td>
                <td>${data['affilation']}</td>
        `

        if(isEmail) {
            htmls += `
                <td>${data['email']}</td>
            `;
        }

        htmls += `</tr>`;
    }
    htmls += `</tbody>`;

    elem.innerHTML = htmls;
}